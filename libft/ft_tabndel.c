#include "libft.h"

void	ft_tabndel(char **t, int len)
{
	int i;

	i = 0;
	if (t && *t)
	{
		while (i < len)
		{
			free(t[i]);
			i++;
		}
		free(t);
	}
	t = NULL;
}
